#include "3-calc.h"

/**
 * op_add - adds 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subs 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: difference between the two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: multiplication of the two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: division of the two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: modulo of the two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
